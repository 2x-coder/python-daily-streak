#Mini Project 1: Daily Temperatures
print("Mini Project 1: Daily Temperatures")
import numpy as np

temps = np.array([30, 31, 29, 35, 34, 33, 32])

print("Average:", np.mean(temps))
print("Highest", np.max(temps))
print("Lowest", np.min(temps))
print("Std Dev", np.std(temps))

